char	*ft_strchr(const char *s, int c)
{
	char	*ps;

	ps = (char *)s;
	while (*ps)
	{
		if (*ps == (char)c)
			return (ps);
		ps++;
	}
	if (*ps == '\0' && c == 0)
		return (ps);
	return (0);
}
