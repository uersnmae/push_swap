char	*ft_strcpy(char *dest, const char *src)
{
	int	_i;

	_i = 0;
	while (src[_i] != '\0')
	{
		dest[_i] = src[_i];
		_i++;
	}
	dest[_i] = '\0';
	return (dest);
}
